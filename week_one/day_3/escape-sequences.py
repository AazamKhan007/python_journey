# new line character
print("What is your name -\nANURODH KUMAR")

# Tab charcter with 4 space
print("Tab starts from here\tends here")

# single quote
print('what month is this \'September\'')

# double quote
print("What day it is \"Friday\"")

# literal backslash
print("path - C:\\User\\Anurodh\\Docs")

# carriage return - ABC (overwrites 123)
print("123jdskfdgkfhd\rABC")

# backspace
print("This month is Sept\b.")
